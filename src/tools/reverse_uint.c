unsigned int		reverse_uint(unsigned int n)
{
	int		swapped;

	swapped = ((n >> 24) & 0xff)
		| ((n << 8) & 0xff0000)
		| ((n >> 8) & 0xff00)
		| ((n << 24) & 0xff000000);
	return (swapped);
}
